function getCurrentLanguage() {
  return localStorage.getItem('language') || 'pt';
}

function setLanguage(lang) {
  localStorage.setItem('language', lang);
  translatePage(lang);
  updateLanguageButton(lang);
  // If modal is open, re-translate its content
  const modal = document.getElementById('projectModal');
  if (modal && !modal.classList.contains('hidden') && window.currentProjectIndex !== undefined && window.currentProjectIndex !== null) {
    const projectCards = Array.from(document.querySelectorAll('.project-card'));
    if (window.currentProjectIndex < projectCards.length) {
      const card = projectCards[window.currentProjectIndex];
      const data = {
        title: translations[lang][card.dataset.title],
        description: translations[lang][card.dataset.description],
        tools: card.dataset.tools,
        images: card.dataset.images ? card.dataset.images.split(',') : [],
        url: card.dataset.url
      };
      // Update modal fields with translated content
      document.getElementById('modalTitle').textContent = data.title;
      document.getElementById('modalDescription').textContent = data.description;
      document.getElementById('modalTools').textContent = translations[lang]['tools_techniques'] + data.tools;
    }
  }
}

function t(key) {
  const lang = getCurrentLanguage();
  return translations[lang][key] || translations['pt'][key] || key;
}

function translatePage(lang) {
  // Translate all elements with data-i18n attribute
  document.querySelectorAll('[data-i18n]').forEach(element => {
    const key = element.getAttribute('data-i18n');
    element.innerHTML = translations[lang][key] || translations['pt'][key] || key;
  });
  
  // Update HTML lang attribute
  document.documentElement.lang = lang === 'pt' ? 'pt-BR' : 'en';
  
  // Update meta tags
  const titleElement = document.querySelector('title');
  if (lang === 'en') {
    titleElement.textContent = 'Robson Moura – Senior UX/UI & Product Designer | Case Studies';
    document.querySelector('meta[name="description"]').setAttribute('content', 'Professional portfolio of Robson Moura – Senior UX/UI & Product Designer with over 15 years of experience in digital experiences, front-end and scalable products. Check out projects and experience.');
  } else {
    titleElement.textContent = 'Robson Moura – Senior UX/UI & Product Designer | Case Studies';
    document.querySelector('meta[name="description"]').setAttribute('content', 'Portfólio profissional de Robson Moura – Senior UX/UI & Product Designer com mais de 15 anos de experiência em experiências digitais, front-end e produtos escaláveis. Confira projetos e experiência.');
  }
}

function updateLanguageButton(lang) {
  const ptBtn = document.getElementById('langPT');
  const enBtn = document.getElementById('langEN');
  
  if (ptBtn && enBtn) {
    if (lang === 'pt') {
      ptBtn.classList.add('text-red-600', 'font-semibold');
      ptBtn.classList.remove('text-slate-400');
      enBtn.classList.remove('text-red-600', 'font-semibold');
      enBtn.classList.add('text-slate-400');
    } else {
      enBtn.classList.add('text-red-600', 'font-semibold');
      enBtn.classList.remove('text-slate-400');
      ptBtn.classList.remove('text-red-600', 'font-semibold');
      ptBtn.classList.add('text-slate-400');
    }
  }
}

// Initialize on page load
document.addEventListener('DOMContentLoaded', () => {
  const lang = getCurrentLanguage();
  translatePage(lang);
  updateLanguageButton(lang);
});